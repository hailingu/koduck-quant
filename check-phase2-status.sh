#!/bin/bash
# Phase 2 任务状态监控脚本

echo "╔════════════════════════════════════════════════════════════════════════════╗"
echo "║                      Phase 2 任务状态监控                                  ║"
echo "╚════════════════════════════════════════════════════════════════════════════╝"
echo ""

# 获取 issue 状态
get_issue_status() {
    local issue_num=$1
    gh issue view $issue_num --json state,closedAt --jq 'if .state == "CLOSED" then "✅ 已完成 (\(.closedAt[:10]))" else "🟡 进行中" end' 2>/dev/null || echo "❓ 未知"
}

echo "📊 当前任务状态："
echo ""
echo "┌────────┬──────────────────────────────────────┬──────────────────────────────┐"
echo "│ Issue  │ 任务                                 │ 状态                         │"
echo "├────────┼──────────────────────────────────────┼──────────────────────────────┤"

for issue in 235 237 238 236 239 240 241; do
    case $issue in
        235) task="P2-01 JaCoCo 覆盖率      " ;;
        236) task="P2-02 测试分层治理      " ;;
        237) task="P2-03 模块边界梳理      " ;;
        238) task="P2-04 PMD 治理          " ;;
        239) task="P2-05 性能基线          " ;;
        240) task="P2-06 一键质量脚本      " ;;
        241) task="P2-07 回归复盘          " ;;
    esac
    
    status=$(get_issue_status $issue)
    printf "│ #%d │ %s │ %s │\n" $issue "$task" "$status"
done

echo "└────────┴──────────────────────────────────────┴──────────────────────────────┘"
echo ""

# 检查可解锁的任务
echo "🔓 依赖检查："
echo ""

# 检查 #236 是否可以启动
if [ "$(get_issue_status 235)" = "✅" ] && [ "$(get_issue_status 236)" != "✅" ]; then
    echo "  ✅ #236 (测试分层) 可以启动 - #235 已完成"
fi

# 检查 #239 是否可以启动
if [ "$(get_issue_status 236)" = "✅" ] && [ "$(get_issue_status 237)" = "✅" ] && [ "$(get_issue_status 239)" != "✅" ]; then
    echo "  ✅ #239 (性能基线) 可以启动 - #236 和 #237 都已完成"
fi

# 检查 #240 是否可以启动
if [ "$(get_issue_status 239)" = "✅" ] && [ "$(get_issue_status 240)" != "✅" ]; then
    echo "  ✅ #240 (一键质量脚本) 可以启动 - #235~#239 已完成"
fi

# 检查 #241 是否可以启动
if [ "$(get_issue_status 240)" = "✅" ] && [ "$(get_issue_status 241)" != "✅" ]; then
    echo "  ✅ #241 (回归复盘) 可以启动 - 全部任务已完成"
fi

echo ""
echo "════════════════════════════════════════════════════════════════════════════"
echo "提示: 每小时运行此脚本检查依赖解锁情况"
echo "════════════════════════════════════════════════════════════════════════════"
